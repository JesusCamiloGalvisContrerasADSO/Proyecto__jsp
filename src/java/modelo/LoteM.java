
package modelo;

    
public class LoteM {
    int id;
    int num;

    public LoteM() {
    }

    public LoteM(int id, int num) {
        this.id = id;
        this.num = num;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    
    
    
}
